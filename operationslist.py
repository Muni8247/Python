#Arithmatic
l=[1,2,3,4,5]
l1=[6,7,8,9,10]
print(l)
print(l1)

#Arithmatic operations use only (+ , *)
#Concatenate//Merge
l2=l+l1
print(l2)

#Multiplication
print("***************Multiplication*****************")
print(l*3)
print(l1*3)

#Membership Operators
l=[1,2,3,4,5]
l1=[6,7,8,[9,10]]
print(l)
print(l1)

print(5 in l)
print(10 in l1)
print([10] in l1)
print([9,10] in l1)

